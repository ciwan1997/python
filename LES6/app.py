# A variable can have a short name (like x and y) or a more descriptive name (age, carname, total_volume). 

# Rules for Python variables:  قوانين لإنشاء متغيرات في البايثون
# A variable name must start with a letter or the underscore character  _   اسم متغير يجب ان يبدا بحرف او علامة ناقص على السطر 
# A variable name cannot start with a number -                                                                                  اسم متغير لا يمكنه بدء ب رقم

# _ اسم متغير يمكنه ان يحتوي فقط على ارقام واحرف انجليزية و علامة ناقص الذي ينكتب على السطر 
# A variable name can only contain alpha-numeric characters and underscores (A-z, 0-9, and _ )

# احرف الصغيرة والكبيرة في الاسم متغيرات حساسة ,يوجد فرق بين حرف كبير وحرف صغير 
# Variable names are case-sensitive (age, Age and AGE are three different variables) 
# اسماء اوامر بايثون  لايمكنك استخدماها كا اسم متغير 
